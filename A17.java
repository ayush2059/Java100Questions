//Simple For Loop Program in Java
package codingpract;
class Star
{
	int i,j;
	public void main()
	{
		for(i=1;i<=5;i++)
		{
			for(j=1;j<=i;j++)
			{
				System.out.print("*");
			}System.out.println();
		}
			
		}
		
	}

public class A17 {

	public static void main(String[] args) {
		Star r=new Star();
		r.main();
		
		
		
	}}
	


