package codingpract;
class Arrays
{
	int arr[]= {8,7,5,3,1,2,3,4};
	public void main()
	{
		for(int a:arr)
		{
			System.out.println(a);
		}
	}
	public void main2()
	{
		String arr[]= {"Ayush","Raj","Adarsh","Raj"};
		for(String a:arr)
		{
			System.out.println(a);
		}
	}
}
public class A24 {

	public static void main(String[] args) {
		Arrays ary=new Arrays();
		ary.main();
		ary.main2();
	}}


