//Print all alphabet using for loop Program in java

package codingpract;

class Alphabet
{
	char ch;
	public void main()
	{
	for(ch='A';ch<='Z';ch++)
	{
		System.out.print(ch+" ");
	}
}}
public class A23 {

	public static void main(String[] args) {
		Alphabet at=new Alphabet();
		at.main();
		
	}

}
